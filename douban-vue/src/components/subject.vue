                                        <p>《圣经》中大卫与歌利亚的故事家喻户晓，在原文本中，这故事的主角是少年大卫和以色列王扫罗。按高尔德自己的话来说，原文中非利士勇士歌利亚只是“一连串描绘他身材的数字和一身铠甲兵器”。汤姆·高尔德将这个故事的叙述角度从以色列人一方转到非利士人一方，为歌利亚这个人物添加了更多个性细节，让他成为一个喜欢文书工作的文静的巨人。歌利亚的形象在高尔德笔下变得鲜活，整个故事的氛围也随之改变。
                                        </p>
                                        <p>⚔️“巨人=好斗”？挑战刻板印象</p>
                                        <p>《圣经》源文本中，体型高大的歌利亚几乎是被默认为生性残暴、好斗，因此最终落得被斩首的下场也是罪有应得。然而在汤姆·高尔德的版本中，歌利亚成了一个讨厌巡逻、讨厌舞刀弄剑、反感别人斗兽取乐、喜欢文书工作的巨人。体型高大≠凶残好斗，在高尔德的妙笔改编下，这个原本理所当然的宗教寓言罩上了一层荒诞又悲凉的色彩。
                                        </p>
