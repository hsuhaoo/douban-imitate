                                <p data-page="0">每当我在各种媒体上看到冯唐的名字时，都会有不一样的感受。</p>
                                <p data-page="0">有时候看他谈论某个问题时，觉得如此真知灼见的人真是世所罕见；而有的时候又觉得他浑身上下都散发着那股“油腻”劲儿，让我这样的钢铁直男都受不了。
                                </p>
                                <p data-page="0">
                                    但无论怎么说，他写的书大多还是蛮有意思的，尤其是看到以“成事心法”为书名时，更是没啥犹豫的就入了。因为无论冯唐为人如何，他这么多年跨界好几次，每次做得事情都称得上风生水起，所以我还是很想看一看他会总结出什么做事心得来。
                                </p>
                                <p data-page="0">
                                    当我把书拿到手后，用了两天时间快速的翻阅了一遍后，决定把它放在自己的床边，闲暇可以拿出来再仔细翻看书中的某些章节——不是为了学习，而是让便于自省。
                                </p>
                                <p data-page="0">
                                    因为全书并没有提供那种让人读了一遍后就拍着大腿喊叫：“原来事情应该这么干！”那样的“技巧”类内容，反而是为大家讲述那些正确到看到那些词就觉得倒胃口的的准则，比如在书中的第一部分，“知己”篇中，冯唐向读者的建议是保持努力、坚持、专心、节制……
                                </p>
                                <p data-page="0">我都可以想象很多人看到这些内容时会立即站出来反驳：</p>
                                <p data-page="0">今天的社会贫富分化、内卷化这么严重，光坚持有什么用？</p>
                                <p data-page="0">我们都996了还要咋努力？</p>
                                <p data-page="0">同事之间不是使绊子就是说风凉话，锅都背到几层楼了，怎么可能专心？</p>
                                <p data-page="0">虽然这些话用来描述今天的社会没有错，但老实说用这些话来解释自己为什么没能成功，就显得太能甩锅了。</p>
                                <p data-page="0">当然，我对冯唐的这个观点这么感同身受，不是因为我也快到了要“油腻”的年纪，而是因为我自己的人生经历就恰好从正反两方面验证了冯唐的理论：</p>
                                <p data-page="0">一方面，我从大城市辞职回到老家后，像很多同龄人一样无奈的听从了家里人的建议考进了体制内。</p>
                                <p data-page="0">
                                    虽然网络上的我表现得云淡风轻，但现实中的我，每天对待工作的态度就是变着花样的摸鱼……所以五年后，同时进入警队的同事中，有人坐进了办公室，有人高升成了副队长。</p>
                                <p data-page="0">
                                    几个臭味相投的同事一起闲聊起这些事时，会说这个人是走了后门、那个人是会拍马屁，但我自己非常明白，即使不谈走后门、拍马屁，我的工作表现也不应该是被提拔的那个人。</p>
                                <p data-page="0">而另一方面，在网络上喜爱写东西的我，因为偶然看到了曾国藩“结硬寨，打呆仗”的名言后，觉得自己也应该学习一下，于是给自己定下一个目标：</p>
                                <p data-page="0">每天写下一件让自己觉得最有趣的事情，再发到社交网络上，以供大家随便评论，看一看五到十年后自己的写作水平会变成什么样子。</p>
                                <p data-page="0">
                                    这个目标在过去的六年时间里，除了因为遭遇疫情外加创业的辛劳让我暂停了几个月外，一直都没间断过。于是我由一名写作水平不咋地的段子手发展成了虽然水平还是不咋地，但似乎挺受编辑欢迎的写手。
                                </p>
                                <p data-page="0">
                                    也正是因为如此，我才需要时不时拿起这本书来告诉自己：写作天赋什么的，都是别人客气客气，真的当真而不去努力的话，立刻就会变成那个没人来看的路人甲了啊！
                                </p>
                                <p data-page="0">
                                    当然，冯唐在这本书中除了告诉大家如何面对自己外，还向大家阐述怎样面对身边人、面对整个世界。不过其核心内容也都是提供大量看似了无新意，但我们自己可能早就忘记应该保持的做事方法。
                                </p>
                                <p data-page="0">可能也只有这样的书，才值得让人反复去读，哪怕作者自我感觉良好的气息已经扑到了我的脸上！</p>
                                <p data-page="0">PS：这本书除了上述作用外，还有一个我已经用不到，但干培训的朋友可能很需要的好处：许多章节的小标题和金句提炼出来，就是个优秀的PPT啊！
                                </p>
